print("Любое число, которые Вы введёте в конечном итоге превратится в 4.") #ИЗМЕНЕНИЕ

num=int(input("Веедите число")) #ИЗМЕНЕНИЯ

mult = num * 2
print("Число умножено на 2", mult)
summ = mult + 8
print("К числу прибавили 8", summ)
sep = summ / 2
print("Число разделили на 2", sep)
res = sep - num
print("Из числа отняли исходное значение", res)
print("Результат = ", int(res))
